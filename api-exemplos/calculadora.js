const somar = function (numeroA, numeroB){
    return numeroA + numeroB



}

const subtrair = function (numeroA, numeroB){
    return numeroA - numeroB



}


const multiplicar = function (numeroA, numeroB){
    return numeroA * numeroB



}

const dividir = function (numeroA, numeroB){
    return numeroA / numeroB



}

//Exportando as funções que criei como módulo
module.exports = {
     somar, subtrair, multiplicar, dividir

}